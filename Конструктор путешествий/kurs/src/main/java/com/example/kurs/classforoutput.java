package com.example.kurs;

public class classforoutput
{
    private String ob;
    private String data;
    private int sum;
     public  classforoutput(String ob, String data, int sum)
     {
         this.ob=ob;
         this.data=data;
         this.sum=sum;
     }
     public classforoutput()
     {

     }

     public String getOb()
     {
         return ob;
     }
     public String getData()
     {
         return data;
     }
     public int getSum()
     {
         return sum;
     }

}
